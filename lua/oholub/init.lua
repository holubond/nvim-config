-- Remaps
require('oholub.remap')

-- Utils
require('oholub.utils')

-- Plugins -- Lazy package manager
local lazypath = vim.fn.stdpath("data") .. "/lazy/lazy.nvim"
if not vim.loop.fs_stat(lazypath) then
  vim.fn.system({
    "git",
    "clone",
    "--filter=blob:none",
    "https://github.com/folke/lazy.nvim.git",
    "--branch=stable", -- latest stable release
    lazypath,
  })
end
vim.opt.rtp:prepend(lazypath)
-- Plugins -- Load plugins from the plugins folder
require("lazy").setup("oholub.plugins")
